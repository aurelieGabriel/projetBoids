void Predateur::velocityPredator()
{
	double vMax=4;

	  
      vx +=(rand()/(double)RAND_MAX)*2 -1;
      vy +=(rand()/(double)RAND_MAX)*2-1;
      double normV = sqrt(vx*vx+vy*vy);
      if(normV>vMax)
      {
		  vx=vx/(normV/vMax);
		  vy=vy/(normV/vMax);
	  }

}
void Predateur::attente()
{
	vx = 0;
    vy = 0;
	a ++;
}

bool Predateur::peutBouger()
{
	bool b;
	if(a >100)
	{
		b=true;
	}
	else
	{
		b=false;
	}
	return b;
}


